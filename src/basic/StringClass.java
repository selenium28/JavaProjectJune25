package basic;

public class StringClass {

	public static void main(String[] args) {
		String str = "Java";
		String abc = "Testing";
		int a = 200;
		int b = 50;
		System.out.println(str);
		System.out.println(a+str);
		System.out.println(a+b);  // 250 
		System.out.println(a+b+str+b);  // 25010050
		System.out.println(str+abc);

	}

}
